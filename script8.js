function startOverview(array) {
    imgOverview[0].innerHTML = "";
    for (let i = 0; i < array[1].length; i++) {
        imgOverview[0].innerHTML += `<img onclick="openOverlay(event)" id="img${i}" class="img imgSmall" src="${array[0]}${array[1][i]}" alt="${array[2][i]}">`;
    } 
    document.getElementById("portugal2021Label").classList.add("styleTitle");
    document.getElementById("portugal2021").checked = true;
    document.getElementById("titleCollection").innerHTML = array[3];
}

function setOverview(array) {
    imgOverview[0].innerHTML = "";
    for (let i = 0; i < array[1].length; i++) {
        imgOverview[0].innerHTML += `<img onclick="openOverlay(event)" id="img${i}" class="img imgSmall" src="${array[0]}${array[1][i]}" alt="${array[2][i]}">`;
    }   
    document.getElementById("titleCollection").innerHTML = array[3];
    document.getElementById("collectionsNav").style.top = "-50px";
    document.getElementById("collectionsNav").style.opacity = 0;
    document.getElementById("arrowUpDown").src = "./img/icons8-expand-arrow-32.png";
    document.getElementById("headerNav").classList.add("closed");
}

function checkMenu() {
    let collectionsNav = document.getElementById("collectionsNav");
    let collectionsNavText = document.getElementById("headerNav");
    let collectionsNavUpDown = document.getElementById("arrowUpDown");

    if(collectionsNavText.classList.contains("closed")) {
        showMenu(collectionsNav, collectionsNavText, collectionsNavUpDown);
    } else if(!collectionsNavText.classList.contains("closed")) {
        showMenuNot(collectionsNav, collectionsNavText, collectionsNavUpDown)
    }
}

function showMenu(collectionsNav, collectionsNavText, collectionNavUpDown) {
    collectionsNav.style.top = "90px";
    collectionsNav.style.opacity = 1;
    collectionNavUpDown.src = "./img/icons8-collapse-arrow-32.png"
    collectionsNavText.classList.remove("closed")
}

function showMenuNot(collectionsNav, collectionsNavText, collectionNavUpDown) {
    collectionsNav.style.top = "-50px";
    collectionsNav.style.opacity = 0;
    collectionNavUpDown.src = "./img/icons8-expand-arrow-32.png"
    collectionsNavText.classList.add("closed")
}

function checkbox(e) {
    let checkboxes = document.querySelectorAll(".checkbox");
    let clickedId = e.target.id;
    let labels = document.querySelectorAll(".label");
    for (let i = 0; i < checkboxes.length; i++) {
        labels[i].classList.remove("styleTitle");
        checkboxes[i].checked = false;
    }
    document.getElementById(`${clickedId}Label`).classList.add("styleTitle");
    document.getElementById(`${clickedId}`).checked = true;
}

function openOverlay(e) {
    document.getElementById("overlayCtn").classList.remove("dNone");
    e.stopPropagation();
    let clickEvent = e;
    let clickedId = clickEvent.target.id;
    let clickedIdNo = parseInt(clickedId.slice(3));
    let clickedOuterHTML = e.target.outerHTML;
    let currentArray = checkArray(clickedOuterHTML)
    setImage(clickedIdNo, currentArray)
}

function setImage(clickedIdNo, currentArray) {
    let srcString = `${currentArray[0]}${currentArray[1][clickedIdNo]}`
    document.getElementById("imgBig").src = srcString;
    let titleString = `${currentArray[2][clickedIdNo]}`
    document.getElementById("imgTitle").innerHTML = titleString;
    let currentCount = clickedIdNo + 1;
    document.getElementById("currentCount").innerHTML = currentCount;
    let totalCount = currentArray[1].length;
    document.getElementById("totalCount").innerHTML = totalCount;
}

function checkArray(clickedOuterHTML) {
    if (clickedOuterHTML.includes("portugal")) {
        return portugal
    } else if (clickedOuterHTML.includes("mallorca")) {
        return mallorca
    } else if (clickedOuterHTML.includes("tuerkei")) {
        return tuerkei
    } else if (clickedOuterHTML.includes("indien")) {
        return indien
    }
}

function closeOverlay(e) {
    e.stopPropagation();
    if (e.target.id === "overlayCtn" || e.target.id === "close") {
        imgOverlay.classList.add("dNone");
    }
}

function checkNextImg(numberUpDown) {
    let oneUpDown = numberUpDown;
    let openedImgOuterHTML = document.getElementById("imgBig").outerHTML;
    let openedArray = checkOpenedArray(openedImgOuterHTML)
    let openedImg = checkOpendedImg(openedImgOuterHTML, openedArray);
    let nextImgIndex = checkNextIndex(openedArray, openedImg, oneUpDown)
    let nextSrcString = `${openedArray[0]}${openedArray[1][nextImgIndex]}`
    let titleString = `${openedArray[2][nextImgIndex]}`
    let nextCount = nextImgIndex + 1;
    let totalCount = openedArray[1].length;
    openNextImg(nextSrcString, titleString, nextCount, totalCount)
}

function checkNextIndex(openedArray, openedImg, oneUpDown) {
    let openedImgIndex = checkOpenedIndex(openedArray, openedImg)
    let newIndex = openedImgIndex + oneUpDown
    if (newIndex === -1) {
        newIndex = openedArray[1].length -1;
    } else if (newIndex === openedArray[1].length) {
        newIndex = 0
    }
    return newIndex
}

function openNextImg(nextSrcString, titleString, nextCount, totalCount) {
    document.getElementById("imgBig").src = nextSrcString;
    document.getElementById("imgTitle").innerHTML = titleString;
    document.getElementById("currentCount").innerHTML = nextCount;
    document.getElementById("totalCount").innerHTML = totalCount;
}

function checkOpenedArray(openedImgOuterHTML) {
    if (openedImgOuterHTML.includes("portugal")) {
        return portugal
    } else if (openedImgOuterHTML.includes("mallorca")) {
        return mallorca
    } else if (openedImgOuterHTML.includes("tuerkei")) {
        return tuerkei
    } else if (openedImgOuterHTML.includes("indien")) {
        return indien
    }
}

function checkOpendedImg(openedImgOuterHTML, openedArray) {
    for (let i = 0; i < openedArray[1].length; i++) {
        if (openedImgOuterHTML.includes(openedArray[1][i])) {
            return openedArray[1][i]
        } 
    }
}

function checkOpenedIndex(openedArray, openedImg) {
    return openedArray[1].indexOf(openedImg);
}